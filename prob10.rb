'''
Problem 10: Iterating with times
Objective: Use the times method to execute a block of code a specified number of times.
Task: Print "Ruby is fun!" five times using the times method.
Example Usage and Expected Output:
Ruby is fun!
Ruby is fun!
Ruby is fun!
Ruby is fun!
Ruby is fun!
'''

5.times do |i|
puts "Ruby is fun!"
end
